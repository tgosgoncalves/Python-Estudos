# Operadores aritimeticos


print('Multiplicação: 10 * 10 =', 10 * 10)
print('Multiplicação por String: 10 * Tiago =', 10 * 'Tiago')  # Repedição da String 10 vezes


print('Soma: 10 + 10 =', 10 + 10)
print('Soma: 10 + Tiago =', 'Tiago' + 'Tiago')  # concatenação = TiagoTiago


print('Subtração: 10 - 5 =', 10 - 5)

print('Divisão: 50 / 10 =', 50 / 10 )

print('Divisão: 10.5 // 3 =', 10.5 / 3)
print('Divisão de inteiro: 10.5 // 3 =', 10.5 // 3)

print('Precedencia')
print(5+2*10)
print((5+2)*10)








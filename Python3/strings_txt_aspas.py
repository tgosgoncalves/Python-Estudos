# string é um tipo primitivo, são textos que estão dentro de aspas (tudo que estiver dentro de aspas)
# str = string

print('Alguma coisa')
print("Alguma coisa")
print(123456)

print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')

# carcteres de escape, possovel utilizar porem codigo fica feio
print("Essa é uma \'string\' (str).")
print('Essa é uma \"string\" (str).')

# o \n quebra linha
print("Esse é meu \n (str).")

# o (r) no inicio diz ao python que não é para executar nada alem do texto
print(r"Esse é meu \n (str).")

